# Questão 5 - Leia um número como texto (input) e: Converta para inteiro; Mostre o triplo.
numero = input("Questão 5 - Digite um número para calcular o triplo: ") # Recebe o número do usuário 
numero = int(numero) # Converte para inteiro

resultado = numero * 3 # Calcula o triplo do número
print("Resultado:", resultado) # Mostra o resultado 