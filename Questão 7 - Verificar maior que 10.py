# Questão 7 - Leia um valor e verifique: Se é maior que 10 → “Maior que 10”; Caso contrário → “Menor ou igual a 10”
numero = float(input("Questão 7 - Digite um número para saber se é maior ou menor que 10: ")) # Recebe o número inteiro do usuário 

if (numero > 10): # Se o numero foi maior que 10
        print("Maior que 10") # Será maior que 10
else: # Se não for
        print("Menor que 10") # Será menor que 10